$(".header__card--1").click(function() {
    $(".header__card").removeClass("header-active");
    $(this).addClass("header-active");
    $(".cards").css("background-color", '#FBE7C6');
    $(".cards__card").removeClass("card--active");
    $(".cards__card--1").addClass("card--active");
    $(".cards__card").css({
      opacity: '0%'
    });
    $(".cards__card--1").css({
      opacity: '100%'
    });
  });
  
